import React from 'react';
import { TrendLarge, TrendSmall } from '../components';
import Container from '../assets/wrappers/TrendContainer'; //general layout, color and loading
import day from 'dayjs';
import advancedFormat from 'dayjs/plugin/advancedFormat';
day.extend(advancedFormat);
/**
 * This component is used by Trends.jsx to display each trend in a trend card with its detail like the chart, trend name etc.
 * This component uses: TrendChartComponent, TrendChart, TrendFlashChart,
 * @param {Object[]} interestOverTime - is set up as keyValueObject {{key: string, value: number}} used in recharts
 * @param {string} trend - trend name
 * @param {string} trendCategory - trend category
 * @param {string} trendTech - the technology used in the trend
 * @param {string} trendDesc - data for storing short description
 * @param {Object[]} createdBy - mongoose.Types.ObjectId user id who added the trend
 * @param {string} slug - string for url link
 * @param {number} views - counts each visit to the trend in incrementViews()
 * @param {boolean} isApproved - if trend succeeds in generating all data this marked as true
 * @param {Date} createdAt - generated by mongodb ex: 2024-01-06T05:00:00.000+00:00
 * @param {Date} updatedAt - generated every time there is update in Model ex: 2024-01-06T05:00:00.000+00:00
 * @param {Object[]} flashChart - set up as keyValueObject {{ value: number}}
 * @param {Object[]} forecast - is set up as keyValueObject {{key: string, value: number}} used in recharts
 * @param {string} generatedBlog - large string with HTML structured data
 * @param {string} trendStatus - trend status based on data
 * @param {string} trendUse - string with more HTML structured data
 * @returns {JSX.Element} The TrendCard component populated with the trend data and chart.
 * @param {string[]} savedTrends is array which is extracted from user object and saved with onSave to db
 * @param {func} onSave - use to track when user clicks bookmark button and does a PATCH
 * @param {boolean} isGridView - this is used to determine if grid-view is used in smaller screens
 * @param {string[]} onRemove - PATCH Delete (only for admin)
 * @param {string[]}
 * @param {string[]}
 * @param {string[]}
 *
 */

const inferenceColorOptions = [
  'var(--inference-color-1)',
  'var(--inference-color-2)',
  'var(--inference-color-3)',
  'var(--inference-color-4)',
  'var(--inference-color-5)',
  'var(--inference-color-6)',
  'var(--inference-color-7)',
  'var(--inference-color-8)',
  'var(--inference-color-9)',
];

const getRandomInferenceColor = () => {
  const randomIndex = Math.floor(Math.random() * inferenceColorOptions.length);
  return inferenceColorOptions[randomIndex];
};

function Trend({
  _id,
  slug,
  trend,
  views,
  onSave,
  onDelete,
  onRemove,
  createdAt,
  createdBy,
  guestUser,
  onApprove,
  trendDesc,
  trendTechs,
  updatedAt,
  flashChart,
  isApproved,
  isFeatured,
  isGridView,
  cateIconUrl,
  chartHeight,
  isAdminPage,
  loadingSlug,
  savedTrends,
  trendStatus,
  isMobileTrend,
  trendCategory,
  chartMarginTop,
  githubUsername,
  onApproveManual,
  openSourceStatus,
  interestOverTime,
  isLargeTrendView,
  chartMarginBottom,
}) {
  //main card overlay color
  const cardOverlayColor = '--card-highlight'; //can be CSS var name
  const cardOverlayGrain = 42; //0 to 100
  const cardOverlayGrainSize = 120; //px grain size
  const safeCardOverlayGrain = Math.min(100, Math.max(0, cardOverlayGrain));
  //end main card overlay color

   //inferenceColorMode styling
  const inferenceColorMode = false; //manual switch for now
  const inferenceColorModeRandom = false; //random single-color overlay mode
  const inferenceRandomColorStrength = 100;//0 to 100, controls random color strength
  const overlayTrigerNoHover = false; // true means overlay is always active and cursor pointer is removed
  const inferenceColorModeAnimation = true; //true = animated, false = static overlay with grain
  const inferenceModeSpeed = 5; //existing speed
  const inferenceColorTransparency = 0.18; //existing transparency
  const inferenceOverlaySize = 420; //existing overlay size
  const inferenceColorProximity = '3.8%'; //existing color closeness
  const inferenceGrain = 48; //0 to 100, controls grain strength
  const inferenceGrainSize = 200; //px, bigger number = larger grain texture
  const safeInferenceGrain = Math.min(100, Math.max(0, inferenceGrain)); //keeps value valid
  //end inferenceColorMode styling

  //styling helpers
  const [randomInferenceColor, setRandomInferenceColor] = React.useState(
  getRandomInferenceColor
  ); //selected random overlay color
  const viewCount = Number(String(views || 0).replaceAll(',', '')); //handles 1000 or "1,000"
  const inferenceStatuses = ['trending', 'breakout', 'static']; //statuses allowed to trigger gradient
  const hasInferenceStatus = inferenceStatuses.includes(
    String(trendStatus || '').toLowerCase()
  ); //checks
  const shouldUseInferenceGradient =
  inferenceColorModeRandom ||
  inferenceColorMode ||
  (viewCount > 50 && hasInferenceStatus);//appling to TrendLarge and TrendSmall
  const handleInferenceMouseEnter = () => {
  if (!inferenceColorModeRandom) return;
  setRandomInferenceColor(getRandomInferenceColor()); //HERE chooses new random color on hover
  };
  const trendContainerClassName = [
    shouldUseInferenceGradient ? 'inference-color-mode' : '',
    inferenceColorModeRandom ? 'inference-random-mode' : '',
    overlayTrigerNoHover ? 'inference-no-hover-trigger' : '',
    !inferenceColorModeAnimation ? 'inference-no-animation' : '',
  ]
    .filter(Boolean)
    .join(' ');
  //styling helpers end
  
  const trendTech   = trendTechs?.[0]?.value ?? '';//primary tech from trendTechs[0] so child props stay unchanged
  const techIconUrl = trendTechs?.[0]?.techIconUrl ?? '';//primary tech icon url from trendTechs[0] so child props stay unchanged
  const upDate = day(updatedAt).format('MM YYYY'); //converting updated at
  const isLoading = loadingSlug === slug; // determining if this specific trend is loading
  const isLargeScreen = window.matchMedia('(min-width: 812px)').matches; // using media query hook to determine the screen size
  // Props for TrendLarge
  const largeProps = {
    _id,
    slug,
    views,
    trend,
    upDate,
    onSave,
    onDelete,
    onRemove,
    onApprove,
    trendTech,
    trendDesc,
    guestUser,
    isLoading,
    createdAt,
    createdBy,
    isApproved,
    isFeatured,
    isAdminPage,
    savedTrends,
    techIconUrl,
    cateIconUrl,
    trendStatus,
    trendCategory,
    githubUsername,
    onApproveManual,
    openSourceStatus,
    interestOverTime,
    overlayColor: cardOverlayColor,
    overlayGrain: safeCardOverlayGrain,
    overlayGrainSize: cardOverlayGrainSize,
  };
  // Props for TrendSmall
  const smallProps = {
    _id,
    slug,
    trend,
    views,
    upDate,
    onSave,
    onRemove,
    onDelete,
    createdBy,
    onApprove,
    trendTech,
    guestUser,
    updatedAt,
    isLoading,
    trendDesc,
    flashChart,
    isApproved,
    isGridView,
    isAdminPage,
    isFeatured,
    savedTrends,
    techIconUrl,
    chartHeight,
    trendCategory,
    isMobileTrend,
    githubUsername,
    chartMarginTop,
    onApproveManual,
    openSourceStatus,
    chartMarginBottom,
    overlayColor: cardOverlayColor,
    overlayGrain: safeCardOverlayGrain,
    overlayGrainSize: cardOverlayGrainSize,
  };

return (
 <Container
  className={trendContainerClassName}
  onMouseEnter={handleInferenceMouseEnter}
  style={{
    '--inference-mode-speed': `${inferenceModeSpeed}s`,
    '--inference-color-transparency': inferenceColorTransparency,
    '--inference-overlay-size': `${inferenceOverlaySize}%`,
    '--inference-color-proximity': inferenceColorProximity,
    '--inference-grain-opacity': safeInferenceGrain / 100,
    '--inference-grain-size': `${inferenceGrainSize}px`,
    '--inference-random-color': randomInferenceColor,
    '--inference-random-color-strength': `${inferenceRandomColorStrength}%`,
  }}
  >
    {isLargeScreen ? (
      isLargeTrendView ? (
        <TrendLarge {...largeProps} />
      ) : (
        <TrendSmall {...smallProps} />
      )
    ) : isLargeTrendView ? (
      <TrendLarge {...largeProps} />
    ) : isGridView ? (
      <TrendSmall {...smallProps} />
    ) : (
      <TrendSmall {...smallProps} />
    )}
  </Container>
);
}

export default Trend;
